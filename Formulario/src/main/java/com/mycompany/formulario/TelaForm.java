    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        String nome = nameField.getText();
        String email = emailField.getText();
        String senha = new String(password.getPassword());
        String city = (String) citySelection.getSelectedItem(); 
        String news =  "Not Selected";
        String updates = "Not Selected";
        String accept = "No";
       
        
       if(checkNews.isSelected() ){
           news = "Selected";
       }
       if(checkUpdates.isSelected()){
           updates = "Selected";
       }
       if(RadioYes.isSelected()){
           accept = "Yes";
       }
       
       String mensagem = "Name: " + nome + " | Email: " + email + " | Password: " + senha + " | Cidade Selecionada: " + city + " | News: " + news + " | Updates: " + updates + " | Accept: " + accept;
        
        JOptionPane.showMessageDialog(this, mensagem);
        
      
        
        
    }//GEN-LAST:event_btnEnterActionPerformed

